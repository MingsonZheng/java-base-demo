//布尔类型
public class Boolean {
    public static void main(String[] args) {
        //演示判断成绩是否通过的案例
        //定义一个布尔变量
        boolean isPass = false;
        if (isPass == true) {
            System.out.println("考试通过，恭喜");
        } else {
            System.out.println("考试没有通过，下次努力");
        }
        //细节：不可以用0或非0的整数替代false和true
        // boolean isPass2 = 0;//错误
        // boolean isPass3 = -1;//错误
    }
}
